#include "libft.h"

// isalnum - alphanumeric character test

int	ft_isalnum(int c)
{
	if ((c >= 97 && c <= 122) || (c >= 65 && c <= 90) || (c >= '0' && c <= '9'))
		return (1);
	return (0);
}
